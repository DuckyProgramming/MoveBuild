class option{
    constructor(layer,player,x,y,type){
        this.layer=layer
        this.player=player
        this.position={x:x,y:y}
        this.type=type

        this.size=1
        this.fade=1
        this.complete=false

        this.anim={complete:0,description:0}
    }
    display(){
        this.layer.push()
        this.layer.translate(this.position.x,this.position.y)
        this.layer.scale(this.size)
        switch(this.type){
            case 0:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                this.layer.line(10,-30,50,0)
                this.layer.line(10,-10,10,-30)
                this.layer.line(10,-10,-40,-10)
                this.layer.line(-40,-10,-40,10)
                this.layer.line(10,10,-40,10)
                this.layer.line(10,10,10,30)
                this.layer.line(10,30,50,0)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Skip',0,60-this.player*120)
            break
            case 1:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                plus(this.layer,10)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Heal',0,60-this.player*120)
            break
            case 2:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                this.layer.rect(0,0,60,80,5)
                this.layer.line(-10,-25,10,-25)
                this.layer.line(-10,-25,-15,-22.5)
                this.layer.line(-15,-22.5,-15,-12.5)
                this.layer.line(-10,-10,-15,-12.5)
                this.layer.line(-10,-10,10,-10)
                this.layer.line(10,-10,15,-12.5)
                this.layer.line(15,-12.5,15,-22.5)
                this.layer.line(15,-22.5,10,-25)
                this.layer.rect(0,7.5,10,35)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Upgrade',0,60-this.player*120)
            break
            case 3:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                this.layer.rect(0,0,80,80)
                plus(this.layer,8)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Max HP',0,60-this.player*120)
            break
            case 4:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                this.layer.ellipse(0,0,56,56)
                this.layer.ellipse(0,0,84,84)
                plus(this.layer,5)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Relic',0,60-this.player*120)
            break
            case 5:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                this.layer.rect(0,0,60,80,5)
                this.layer.line(-20,-20,-15,-20)
                this.layer.line(-15,-20,0,-5)
                this.layer.line(5,0,20,15)
                this.layer.line(20,15,20,20)
                this.layer.line(15,20,20,20)
                this.layer.line(-20,-15,-5,0)
                this.layer.line(0,5,15,20)
                this.layer.line(-20,-20,-20,-15)
                this.layer.line(-20,20,-15,20)
                this.layer.line(-15,20,0,5)
                this.layer.line(5,0,20,-15)
                this.layer.line(20,-15,20,-20)
                this.layer.line(15,-20,20,-20)
                this.layer.line(-20,15,-5,0)
                this.layer.line(0,-5,15,-20)
                this.layer.line(-20,20,-20,15)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Remove',0,60-this.player*120)
            break
            case 6:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                this.layer.beginShape()
                for(let a=0,la=16;a<la;a++){
                    this.layer.vertex(sin(360*a/la)*(50-a%2*25),cos(360*a/la)*(50-a%2*25))
                }
                this.layer.endShape(CLOSE)
                this.layer.triangle(0,-21,-12,15,12,15)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Strength',0,60-this.player*120)
            break
            case 7:
                this.layer.stroke(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade)
                this.layer.strokeWeight(3)
                this.layer.noFill()
                this.layer.rect(0,0,60,80,5)
                plus(this.layer,5)
                this.layer.noStroke()
                this.layer.fill(mergeColor([255,125,0],[50,255,50],this.anim.complete)[0],mergeColor([255,125,0],[50,255,50],this.anim.complete)[1],mergeColor([255,125,0],[50,255,50],this.anim.complete)[2],this.fade*max(this.anim.complete,this.anim.description))
                this.layer.textSize(24)
                this.layer.text('Card',0,60-this.player*120)
            break
        }
        this.layer.pop()
    }
    update(){
        this.size=smoothAnim(this.size,dist(inputs.rel.x,inputs.rel.y,this.position.x,this.position.y)<50,1,1.5,5)
        this.anim.description=smoothAnim(this.anim.description,dist(inputs.rel.x,inputs.rel.y,this.position.x,this.position.y)<50,0,1,5)
        this.anim.complete=smoothAnim(this.anim.complete,this.complete,0,1,5)
    }
}